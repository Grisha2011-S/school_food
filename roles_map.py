"""Централизованный маппинг ролей и helper для отображения названий ролей.

Сюда можно добавлять новые роли и их отображаемые имена. При необходимости
в будущем достаточно изменить только этот файл.
"""
from typing import Dict

ROLE_MAP: Dict[str, str] = {
    # ключи ролей (используются в session['role'] и коде) -> отображаемое имя
    'cook': 'Диетсестра',
    'parent': 'Родитель',
    'teacher': 'Учитель',
    'student': 'Ученик',
    'admin': 'Администратор'
}

def get_role_display(role_key: str) -> str:
    """Возвращает отображаемое имя роли по ключу.

    Если роль не найдена в маппинге — возвращается сам ключ.
    """
    return ROLE_MAP.get(role_key, role_key)
